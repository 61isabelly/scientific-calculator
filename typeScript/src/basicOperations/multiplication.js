// Função para multiplicar dois números
function multiplicar(a, b) {
    return a * b;
}
// Exemplo de uso da função
var num1 = 5;
var num2 = 10;
var resultado = multiplicar(num1, num2);
console.log("O resultado da multiplica\u00E7\u00E3o de ".concat(num1, " e ").concat(num2, " \u00E9: ").concat(resultado));
